void LCD_WriteByte(uint8_t data)
{
    wiringPiSPIDataRW(SPI_CHANNEL, &data, 1);
}

void LCD_Write2Byte(uint16_t data)
{
    wiringPiSPIDataRW(SPI_CHANNEL, (uint8_t*)&data, 2);
}

void LCD_WriteMostByte(uint8_t* data, uint32_t len)
{
	while (len > 4096)
	{
		wiringPiSPIDataRW(SPI_CHANNEL, data, 4096);
		data += 4096;
		len -= 4096;
	}
	wiringPiSPIDataRW(SPI_CHANNEL, data, len);
}

void LCD_WriteCom(uint8_t com)
{
	digitalWrite(ST7735_DC, LOW);
	LCD_WriteByte(com);
}

void LCD_WriteData(uint8_t data)
{
	digitalWrite(ST7735_DC, HIGH);
	LCD_WriteByte(data);
}

void LCD_WriteData_16Bit(uint16_t data)
{
	digitalWrite(ST7735_DC, HIGH);
	LCD_Write2Byte(data);
}

void LCD_Reset()
{
    digitalWrite(ST7735_RST, LOW);
    delay(100);
    digitalWrite(ST7735_RST, HIGH);
    delay(50);
}

