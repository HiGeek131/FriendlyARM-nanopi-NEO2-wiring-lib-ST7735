void LCD_SetRegion(uint16_t x_start,uint16_t y_start,uint16_t x_end,uint16_t y_end)
{
	LCD_WriteCom(0x2a);
	LCD_WriteData(0x00);
	LCD_WriteData(x_start+1);
	LCD_WriteData(0x00);
	LCD_WriteData(x_end+1);
	
	LCD_WriteCom(0x2b);
	LCD_WriteData(0x00);
	LCD_WriteData(y_start+0x1A);
	LCD_WriteData(0x00);
	LCD_WriteData(y_end+0x1A);
}

void LCD_DrawPoint(uint16_t x,uint16_t y,uint16_t Data)
{
	LCD_SetRegion(x,y,x+1,y+1);
	LCD_WriteCom(0x2C);
	LCD_WriteData_16Bit(Data);
}

void LCD_DisplayImage(uint16_t x_start,uint16_t y_start,uint16_t x_end,uint16_t y_end, uint16_t* image, uint16_t len)
{
	LCD_SetRegion(x_start, y_start, x_end, y_end);
	LCD_WriteCom(0x2C);
	digitalWrite(ST7735_DC, HIGH);
	LCD_WriteMostByte((uint8_t*)image, len);
}

void LCD_ClearBuffer(uint16_t Color)
{
	for (int i = 0; i < 12800; i++)
    {
        st7735_cfg.displayBuffer[i] = Color;
    }
}

void LCD_DisplayBuffer(void)
{
	uint16_t sendBuffer[12800];
	for(uint16_t i = 0; i < 12800; i ++)
		sendBuffer[i] = st7735_cfg.displayBuffer[i];

	LCD_DisplayImage(0, 0, X_MAX_PIXEL-1, Y_MAX_PIXEL-1, sendBuffer, 25600);
}

void LCD_ClearScreen(uint16_t Color)               
{
	LCD_ClearBuffer(Color);
	LCD_DisplayBuffer();
}

void LCD_ConvertASCIIToBuffer(uint8_t line, uint8_t* p, uint16_t wordColor, uint16_t backColor)
{
	uint8_t a = 0;
	for(a = 0; a < 26 && p[a] >= ' ' && p[a] <= '~'; a++)
	{
		for(uint8_t x = 0; x < 6; x ++)
		{
			for (uint8_t i = 0; i < 8; i++)
			{
				st7735_cfg.displayBuffer[(line * 8 + i) * 160 + a * 6 + x] = zi[p[a] - 32][x] >> i & 0x01 ? wordColor : backColor;
			}
		}
	}
	for(uint8_t i = 0; i < 8; i++)
	{
		for(uint8_t x = a * 6; x < 160; x++)
		{
			st7735_cfg.displayBuffer[(line * 8 + i) * 160 + x] = backColor;
		}	
	}
}
